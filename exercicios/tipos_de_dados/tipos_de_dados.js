// Três valores com strings. Aspas duplas, semples e template literals
console.log('Ivan');
console.log("Santos");
console.log(`Ivan Santos tem ${15} anos`);

// Três valores com numbers. Inteiro, decimal e utilizando operadores aritméticos.
console.log(15);
console.log(15.5);
console.log(50 + 50);

// Três comparações que resultem em boolean
console.log(15 > 20);
console.log(50 < 15);
console.log(15 != 15);

// Três comparações com operadores lógicos
console.log(15 > 20 && 20 != 50);
console.log(20 != 20 || 50 > 20);
console.log(!(50>=50));

