// Strings

// Podem ser impressas com crase, aspas simples e aspas duplas
console.log('Ivan')  // String
console.log("Ivan")  // String
console.log(typeof `Ivan`)  // String template literals

// Quebra de linha
console.log('Ivan Santos de Jesus \n Júnior')

// Utilizando template literals
console.log(`A soma de 2 + 2 é ${2 + 2}.`)  // A soma de 2 + 2 é 4.
console.log(`A multiplicação de 5 vezes 5 é ${5*5}.`) // A multiplicação de 5 vezes 5 é 25. 

// Utilizando aspas nas strings
console.log('O nome dele é "Ivan Santos".')  // O nome dele é "Ivan Santos".
