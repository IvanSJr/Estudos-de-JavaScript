// Numbers

// Mostra no console o tipo de dado 15 -- number
console.log(15);
console.log(typeof 15);
// Mostra no console o tipo de dado 15.2 -- number
console.log(15.2);
console.log(typeof 15.2);
// Mostra no console o tipo de dado -15.2 -- number
console.log(-15.2);
console.log(typeof -15.2);

// Strings

// Podem ser impressas com crase, aspas simples e aspas duplas
console.log('Ivan')  // String
console.log("Ivan")  // String
console.log(typeof `Ivan`)  // String template literals